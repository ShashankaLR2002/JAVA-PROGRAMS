class Double{
    public static void main(String[] args) {
        {
            double pi = 3.14159;
            System.out.println("Pi: " + pi);
        }
        {
            double gravity = 9.81;
            System.out.println("Gravity: " + gravity);
        }
        {
            double speedOfLight = 299792458;
            System.out.println("Speed of Light: " + speedOfLight);
        }
        {
            double electronMass = 9.10938356e-31;
            System.out.println("Electron Mass: " + electronMass);
        }
        {
            double avogadroNumber = 6.02214076e23;
            System.out.println("Avogadro's Number: " + avogadroNumber);
        }
        {
            double goldenRatio = 1.61803;
            System.out.println("Golden Ratio: " + goldenRatio);
        }
        {
            double plankConstant = 6.62607015e-34;
            System.out.println("Planck's Constant: " + plankConstant);
        }
        {
            double eulerNumber = 2.71828;
            System.out.println("Euler's Number: " + eulerNumber);
        }
        {
            double atomicMassUnit = 1.66053906660e-27;
            System.out.println("Atomic Mass Unit: " + atomicMassUnit);
        }
        {
            double gasConstant = 8.314;
            System.out.println("Gas Constant: " + gasConstant);
        }
    }
}