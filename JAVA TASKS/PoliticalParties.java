class PoliticalParties {
	
	public static void main(String[] args) {
		
		System.out.println("starts with PoliticalParties"); 
		
		String[] parties = {"JDS", "BJP", "Indian National Congress", "Bharatiya Janata Party", "Aam Aadmi Party"};
		
		for (int political = 0; political < parties.length; political++) {
			
			System.out.println(parties[political]);
		}
		System.out.println("ends with PoliticalParties");
		
	}

}